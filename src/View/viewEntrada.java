    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        try {
            if (cbxProduto != null && cbxProduto.getSelectedIndex() != -1 && cbxProduto.getSelectedItem().toString().trim().length() > 0) {
                if (txtValor != null && txtValor.getText() != null && txtValor.getText().trim().length() > 0) {
                    int quantidade = Integer.valueOf(txtValor.getText().replace(".", "").replace(",", ""));
                    int m = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente dar entrada em " + quantidade + " unidade(s) do produto " + cbxProduto.getSelectedItem().toString() + "?", "Confirmar", JOptionPane.YES_NO_OPTION);
                    if (m == JOptionPane.YES_OPTION) {
                        EntradaSaida es = new EntradaSaida();
                        Produto produto = ProdutoDaoImpl.getInstance().FindByName(cbxProduto.getSelectedItem().toString());
                        produto.setQuantidade(produto.getQuantidade() + quantidade);
                        es.setProduto(produto);
                        es.setUsuario(Controller.Controller.getUserLogado());
                        es.setAtualizacao(new Date());
                        es.setQuantidade(quantidade);
                        es.setTipoEntradaSaida(TipoEntradaSaida.ENTRADA);
                        es = EntradaSaidaDaoImpl.getInstance().SalvarEntradaSaida(es);
                        produto = ProdutoDaoImpl.getInstance().AlterarProduto(produto);
                        JOptionPane.showMessageDialog(rootPane, "Entrada inserida com sucesso!" , "Sucesso" , JOptionPane.INFORMATION_MESSAGE);
                        LogsDaoImpl.getInstance().CreateLog("O usu√°rio : "
                                + Controller.Controller.getUserLogado().getNome() + "/" 
                                + Controller.Controller.getUserLogado().getMatricula() + " Deu entrada em " 
                                + quantidade + " unidade(s) do produto : " + produto.getProduto(), new Date(), Severidade.INSERIR);
                    
                        txtValor.setText("");
                        cbxProduto.setSelectedIndex(-1);
                    } 
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Por favor, informe a quantidade!", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Por favor, selecione um produto!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
            LogsDaoImpl.getInstance().CreateLog("Ocorreu um erro ao dar entrada no produto : " + cbxProduto.getSelectedItem().toString() + " erro: " + e.getLocalizedMessage(), new Date(), Severidade.INFO);
        }
    }//GEN-LAST:event_btnInserirActionPerformed

